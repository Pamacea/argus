/// Initialize ARGUS
pub async fn cmd_init(no_rules: bool) -> Result<()> {
    let home = std::env::home_dir().ok_or_else(|| anyhow::anyhow!("Cannot determine home directory"))?;

    // Create ~/.argus directory
    let argus_dir = home.join(".argus");
    fs::create_dir_all(&argus_dir)?;

    // Create index directory
    let index_dir = argus_dir.join("index");
    fs::create_dir_all(&index_dir)?;

    // Initialize database
    let _memory = MemoryEngine::new().await?;

    success(&format!("Created ARGUS data directory: {}", argus_dir.display()));

    // Inject rules to ~/.claude/rules/
    if !no_rules {
        let rules_dir = home.join(".claude").join("rules");
        fs::create_dir_all(&rules_dir)?;

        let rules_path = rules_dir.join("argus.md");

        let rules_content = r#"# ARGUS - Omniscient Memory Sentinel

> **Version:** 0.8.0 | **CLI:** `argus`

---

## 🎯 Purpose

ARGUS maintains semantic memory of all your Claude Code sessions.
**Always consult ARGUS before** exploring code or creating solutions.

## 🔧 Integration

ARGUS is installed as a CLI tool. Available commands:
- `argus recall "pattern"` - Search past transactions
- `argus remember "description"` - Save to memory
- `argus index` - Index current project
- `argus stats` - View statistics

## ✅ Mandatory Workflow

### Before ANY Explore or CreateTeam action:

```bash
# Step 1: Search ARGUS memory
argus recall "<what you're looking for>"

# Step 2: Review results
# Step 3: Proceed with action using context
```

### After ANY significant action:

```bash
# Save the result for future reference
argus remember "What you did and why"
```

## 🚫 Never

- Explore without checking ARGUS first
- CreateTeam without searching past solutions
- Ignore patterns found in memory
- Skip remembering important decisions

## 📊 Memory Location

All data stored in: `~/.argus/`
- Transactions: `~/.argus/memory.db`
- Index: `~/.argus/index/`

---

*Auto-generated by ARGUS v0.8.0*
"#;

        fs::write(&rules_path, rules_content)?;

        success(&format!("Injected Claude Code rules: {}", rules_path.display()));
    } else {
        print_info("Skipped rules injection (--no-rules)");
    }

    success("ARGUS is ready! Use 'argus --help' to see available commands.");
    print_info("\nQuick start:");
    print_info("  argus remember \"Did something important\"");
    print_info("  argus recall \"something\"");

    Ok(())
}

/// Remember a transaction
pub async fn cmd_remember(description: String, tags: Option<String>, category: Option<String>) -> Result<()> {
    let memory = MemoryEngine::new().await?;

    // Build context
    let cwd = env::current_dir()?
        .to_str()
        .ok_or_else(|| anyhow::anyhow!("Invalid UTF-8 in path"))?
        .to_string();

    let context = Context {
        cwd: cwd.clone(),
        platform: std::env::consts::OS.to_string(),
        session_id: None,
        project_path: Some(cwd),
        git_branch: None,
        git_commit: None,
    };

    // Build transaction
    let mut metadata = Metadata::default();

    if let Some(tags_str) = tags {
        metadata.tags = tags_str.split(',').map(|s| s.trim().to_string()).collect();
    }

    if let Some(cat) = category {
        metadata.category = Some(cat);
    }

    metadata.summary = Some(description.clone());
    metadata.intent = Some("manual".to_string());

    let tx = Transaction::user(description, context).with_metadata(metadata);

    // Store
    let id = memory.remember(tx).await?;

    success(&format!("Remembered as transaction #{}", id));

    Ok(())
}

/// Recall/search transactions
pub async fn cmd_recall(query: String, limit: usize, full: bool) -> Result<()> {
    let memory = MemoryEngine::new().await?;

    let results = memory.recall(&query, limit).await?;

    if results.is_empty() {
        print_info(&format!("No results found for: {}", cyan(&query)));
        return Ok(());
    }

    success(&format!("Found {} result(s) for: {}", results.len(), cyan(&query)));

    for tx in &results {
        print_transaction(tx, full);
    }

    Ok(())
}

/// List recent transactions
pub async fn cmd_list(limit: usize) -> Result<()> {
    let memory = MemoryEngine::new().await?;

    let results = memory.list(limit, 0).await?;

    if results.is_empty() {
        print_info("No transactions found.");
        return Ok(());
    }

    for tx in &results {
        print_transaction(tx, false);
    }

    Ok(())
}

/// Show a specific transaction
pub async fn cmd_show(id: i64) -> Result<()> {
    let memory = MemoryEngine::new().await?;

    match memory.get(id).await? {
        Some(tx) => {
            print_transaction(&tx, true);
            Ok(())
        }
        None => {
            error(&format!("Transaction #{} not found", id));
            Ok(())
        }
    }
}

/// Index current project
pub async fn cmd_index(_path: Option<PathBuf>, _force: bool) -> Result<()> {
    let indexer = ProjectIndexer::new().await?;
    let cwd = env::current_dir()?;

    print_info(&format!("Indexing: {}", cwd.display()));

    let stats = indexer.index(&cwd).await?;

    success(&format!(
        "Indexed {} files ({} bytes) in {}ms",
        green(&stats.files_indexed.to_string()),
        cyan(&format_size(stats.total_bytes)),
        yellow(&stats.duration_ms.to_string())
    ));

    Ok(())
}

/// Show statistics
pub async fn cmd_stats() -> Result<()> {
    let memory = MemoryEngine::new().await?;
    let stats = memory.stats().await?;

    println!("\n  📊 ARGUS Statistics\n");

    println!("  Transactions: {}", green(&stats.total_transactions.to_string()));
    println!("  Total Size:   {}", cyan(&format_size(stats.total_size_bytes as usize)));

    if let Some(oldest) = stats.oldest_transaction {
        let date_str = oldest.format("%Y-%m-%d %H:%M").to_string();
        println!("  Oldest:      {}", yellow(&date_str));
    }

    if let Some(newest) = stats.newest_transaction {
        let date_str = newest.format("%Y-%m-%d %H:%M").to_string();
        println!("  Newest:      {}", yellow(&date_str));
    }

    println!("\n  By Type:");
    for (ty, count) in &stats.transactions_by_type {
        println!("    {}: {}", ty, count);
    }

    println!();

    Ok(())
}

