/// Prune old transactions
pub async fn cmd_prune(before: String, dry_run: bool) -> Result<()> {
    let duration = parse_duration(&before)?;
    let cutoff = Utc::now() - duration;

    print_info(&format!("Pruning transactions older than: {}", cutoff.format("%Y-%m-%d %H:%M")));

    if dry_run {
        print_info("Dry run - no changes will be made");
        return Ok(());
    }

    let memory = MemoryEngine::new().await?;
    let count = memory.prune(cutoff).await?;

    success(&format!("Pruned {} transaction(s)", count));

    Ok(())
}

/// Reset all data
pub async fn cmd_reset(confirm: bool) -> Result<()> {
    if !confirm {
        crate::cli::output::warn("This will delete all ARGUS data!");
        crate::cli::output::warn("Run with --confirm to proceed.");
        return Ok(());
    }

    let home = std::env::home_dir().ok_or_else(|| anyhow::anyhow!("Cannot determine home directory"))?;
    let argus_dir = home.join(".argus");

    if argus_dir.exists() {
        fs::remove_dir_all(&argus_dir)?;
        success(&format!("Removed: {}", argus_dir.display()));
    } else {
        print_info("No ARGUS data found to remove.");
    }

    // Also remove rules
    let rules_path = home.join(".claude").join("rules").join("argus.md");
    if rules_path.exists() {
        fs::remove_file(&rules_path)?;
        success(&format!("Removed: {}", rules_path.display()));
    }

    print_info("Run 'argus init' to start fresh.");

    Ok(())
}

