// Helper functions

fn print_transaction(tx: &Transaction, full: bool) {
    let id = tx.id.unwrap_or(0);
    let summary = tx.metadata.as_ref()
        .and_then(|m| m.summary.as_ref())
        .map(|s| s.as_str())
        .unwrap_or(&tx.prompt);

    println!(
        "\n  {} {}",
        dim(&format!("#{}", id)),
        summary
    );

    if let Some(created) = tx.created_at {
        let created_str = created.format("%Y-%m-%d %H:%M").to_string();
        println!(
            "  {} {}",
            dim("at:"),
            dim(&created_str)
        );
    }

    if full {
        if let Some(meta) = &tx.metadata {
            if !meta.tags.is_empty() {
                println!("  tags: {}", meta.tags.join(", "));
            }
            if let Some(intent) = &meta.intent {
                println!("  intent: {}", intent);
            }
        }
        println!("  prompt: {}", tx.prompt);
        if let Some(project) = &tx.context.project_path {
            println!("  project: {}", project);
        }
    }
}

fn format_size(bytes: usize) -> String {
    const KB: usize = 1024;
    const MB: usize = KB * 1024;
    const GB: usize = MB * 1024;

    if bytes >= GB {
        format!("{:.2} GB", bytes as f64 / GB as f64)
    } else if bytes >= MB {
        format!("{:.2} MB", bytes as f64 / MB as f64)
    } else if bytes >= KB {
        format!("{:.2} KB", bytes as f64 / KB as f64)
    } else {
        format!("{} B", bytes)
    }
}

fn parse_duration(s: &str) -> Result<Duration> {
    let s = s.to_lowercase();
    let (num, unit) = if s.ends_with('d') {
        (s.trim_end_matches('d'), "days")
    } else if s.ends_with('w') {
        (s.trim_end_matches('w'), "weeks")
    } else if s.ends_with('m') {
        (s.trim_end_matches('m'), "days") // months -> 30 days
    } else if s.ends_with('y') {
        (s.trim_end_matches('y'), "days") // years -> 365 days
    } else {
        return Err(anyhow::anyhow!("Invalid duration format. Use: 30d, 3m, 1y"));
    };

    let n: i64 = num.parse()
        .map_err(|_| anyhow::anyhow!("Invalid number: {}", num))?;

    let days = match unit {
        "days" => n,
        "weeks" => n * 7,
        _ => return Err(anyhow::anyhow!("Unknown unit: {}", unit)),
    };

    Ok(Duration::days(days))
}

/// Install or uninstall Claude Code hooks
pub async fn cmd_install(uninstall: bool) -> Result<()> {
    if uninstall {
        hooks::uninstall_hooks()?;
        success("ARGUS hooks uninstalled");
        print_info("Run 'argus install' to reinstall");
    } else {
        hooks::install_hooks()?;
        success("ARGUS hooks installed");
        print_info("Hooks will activate in the next Claude Code session");
    }

    Ok(())
}

/// Daemon: Start the ARGUS agent
pub async fn cmd_daemon_start(background: bool, foreground: bool) -> Result<()> {
    #[cfg(feature = "agent")]
    {
        use crate::agent::daemon;

        if daemon::is_daemon_running() {
            success("ARGUS Agent is already running");
            let status = daemon::daemon_status()?;
            if let Some(pid) = status.pid {
                println!("  PID: {}", pid);
            }
            if let Some(uptime) = status.uptime {
                println!("  Uptime: {}s", uptime);
            }
            return Ok(());
        }

        if foreground {
            // Run in foreground (for debugging)
            success("Starting ARGUS Agent in foreground...");
            let agent = daemon::AgentDaemon::new().await?;
            agent.run().await?;
        } else if background {
            // Run in background (detached, for auto-start from hooks)
            daemon::start_daemon_background()?;
            success("ARGUS Agent started in background");
        } else {
            // Default: interactive start (foreground with message)
            success("Starting ARGUS Agent...");
            println!("Press Ctrl+C to stop");
            let agent = daemon::AgentDaemon::new().await?;
            agent.run().await?;
        }

        Ok(())
    }

    #[cfg(not(feature = "agent"))]
    {
        error("Agent feature not enabled");
        error("Rebuild with: cargo build --release --features agent");
        Err(anyhow::anyhow!("Agent feature not enabled"))
    }
}

/// Daemon: Stop the ARGUS agent
pub async fn cmd_daemon_stop() -> Result<()> {
    #[cfg(feature = "agent")]
    {
        use crate::agent::daemon;

        if !daemon::is_daemon_running() {
            print_info("ARGUS Agent is not running");
            return Ok(());
        }

        daemon::stop_daemon()?;
        success("ARGUS Agent stopped");

        Ok(())
    }

    #[cfg(not(feature = "agent"))]
    {
        error("Agent feature not enabled");
        Err(anyhow::anyhow!("Agent feature not enabled"))
    }
}

/// Daemon: Check status
pub async fn cmd_daemon_status() -> Result<()> {
    #[cfg(feature = "agent")]
    {
        use crate::agent::daemon;

        let status = daemon::daemon_status()?;

        if status.running {
            success("ARGUS Agent is running");
            println!("\n  📊 Daemon Status:");
            if let Some(pid) = status.pid {
                println!("  PID: {}", pid);
            }
            if let Some(uptime) = status.uptime {
                let mins = uptime / 60;
                let secs = uptime % 60;
                println!("  Uptime: {}m {}s", mins, secs);
            }
            if let Some(version) = status.version {
                println!("  Version: {}", version);
            }
            println!("  IPC: {}", crate::agent::ipc::IpcTransport::for_platform()?.path());
        } else {
            print_info("ARGUS Agent is not running");
            print_info("Start it with: argus daemon start");
            print_info("Or enable auto-start with: argus install");
        }

        Ok(())
    }

    #[cfg(not(feature = "agent"))]
    {
        error("Agent feature not enabled");
        Err(anyhow::anyhow!("Agent feature not enabled"))
    }
}

/// Daemon: Ping the agent
pub async fn cmd_daemon_ping() -> Result<()> {
    #[cfg(feature = "agent")]
    {
        use crate::agent::ipc::IpcClient;

        match IpcClient::new() {
            Ok(client) => {
                if client.ping() {
                    success("ARGUS Agent is responding");
                    Ok(())
                } else {
                    error("ARGUS Agent did not respond");
                    Err(anyhow::anyhow!("Agent not responding"))
                }
            }
            Err(e) => {
                error(&format!("Failed to connect to agent: {}", e));
                Err(e)
            }
        }
    }

    #[cfg(not(feature = "agent"))]
    {
        error("Agent feature not enabled");
        Err(anyhow::anyhow!("Agent feature not enabled"))
    }
}
